/**
 * Regenerates src/components/design/claude-frame-paths.ts from the frame
 * SVGs in public/projects/claude-mark/ and public/projects/claude-thinking/.
 * Run after editing any frame: node scripts/extract-claude-frames.mjs
 *
 * The outlined plate animation on /design renders frames as inline stroked
 * SVGs (a CSS mask can't produce an outline), so the path data must be
 * inlined. viewBoxes are padded so a stroke on edge-touching paths is not
 * clipped.
 */
import { readFileSync, writeFileSync } from "node:fs";

const PAD = 8;
const OUT = "src/components/design/claude-frame-paths.ts";

function frame(path) {
  const svg = readFileSync(path, "utf8");
  const paths = [...svg.matchAll(/<path d="([^"]+)"/g)].map((m) => m[1]);
  const viewBox = svg.match(/viewBox="([^"]+)"/)[1];
  const [x, y, w, h] = viewBox.split(" ").map(Number);
  return {
    viewBox: `${x - PAD} ${y - PAD} ${w + 2 * PAD} ${h + 2 * PAD}`,
    paths,
  };
}

const mark = Array.from({ length: 8 }, (_, i) =>
  frame(`public/projects/claude-mark/frame-${i + 1}.svg`),
);
const thinkingOrder = [2, 3, 4, 5, 6, 7, 8, 9, 10, "base"];
const thinking = thinkingOrder.map((n) => frame(`public/projects/claude-thinking/frame-${n}.svg`));
const base = frame("public/projects/claude-mark/frame-base.svg");

const out = `// Generated by scripts/extract-claude-frames.mjs from
// public/projects/claude-mark/*.svg and public/projects/claude-thinking/*.svg.
// Do not edit by hand — edit the frame SVGs and re-run the script.
// viewBoxes are padded ${PAD} units so a stroke on edge-touching paths is
// not clipped.

export type ClaudeFramePaths = { viewBox: string; paths: string[] };

export const CLAUDE_BASE_FRAME_PATHS: ClaudeFramePaths = ${JSON.stringify(base)};

// Same sets and frame order as CLAUDE_ANIMATION_SETS in ClaudeSpinner.tsx.
export const CLAUDE_FRAME_PATH_SETS: readonly ClaudeFramePaths[][] = [
  ${JSON.stringify(mark)},
  ${JSON.stringify(thinking)},
];
`;

writeFileSync(OUT, out);
console.log(`${OUT} regenerated (${out.length} bytes)`);
